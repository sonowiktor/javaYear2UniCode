
public class GradeMark {

	public static void main(String[] args) {
		int mark = 93;
		if (mark>70) {
			System.out.println("Distinction.");
		} else if (mark>=60) {
			System.out.println("Merit");
		} else if (mark>=40) {
			System.out.println("Pass");
		} else {
			System.out.println("Fail");
		}

	}

}
